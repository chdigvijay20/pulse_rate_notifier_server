package com.kit.pulse.vo;

public class NormalRestingHeartRateVO {
	
	private Integer age;
	private Integer minimumPulseRate;
	private Integer maximumPulseRate;
	
	public Integer getAge() {
		return age;
	}
	
	public void setAge(Integer age) {
		this.age = age;
	}
	
	public Integer getMinimumPulseRate() {
		return minimumPulseRate;
	}
	
	public void setMinimumPulseRate(Integer minimumPulseRate) {
		this.minimumPulseRate = minimumPulseRate;
	}
	
	public Integer getMaximumPulseRate() {
		return maximumPulseRate;
	}
	
	public void setMaximumPulseRate(Integer maximumPulseRate) {
		this.maximumPulseRate = maximumPulseRate;
	}
	
}
